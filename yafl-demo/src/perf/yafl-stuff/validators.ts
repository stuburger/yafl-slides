export const required = (value: any): string | void => {
  if (value === null || value === undefined || value === "") {
    return "This field is required"
  }
}

export const minLength = (length: number) => (value: any): string | void => {
  if (value.length < length) {
    return `This field should be at least ${length} characters.`
  }
}

export const min = (num: number, msg?: string) => (
  value: any
): string | void => {
  if (typeof value === 'number' && value < num) {
    return msg || `Should be at least ${num}.`
  }
}

